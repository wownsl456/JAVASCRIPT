function expressMultiplication(x, y){
    console.log(`${x} * ${y} = ${x * y}`);
  }
  
  // 테스트 코드
  expressMultiplication(3, 4);
  expressMultiplication(3, 2);
  expressMultiplication(7, 5);
  expressMultiplication(8, 9);
  expressMultiplication(5, 5);
  expressMultiplication(9, 9);
  /* 결과값 = * 3 * 4 = 12
            * 3 * 2 = 6 
            * 7 * 5 = 35
            * 8 * 9 = 72
            * 5 * 5 = 25
            * 9 * 9 = 81 */